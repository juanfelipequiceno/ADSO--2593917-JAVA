      public void imprimirUsuarios(){

        for(int i=0;i<this.ventanaMenu.indexproducto;i++){
            if(this.ventanaMenu.listaProducto[i]!=null){
                JLabel etq_lista = new JLabel( i+ " - "+this.ventanaMenu.listaProducto[i].getId()+" - "+this.ventanaMenu.listaProducto[i].getNombre()+" - "+this.ventanaMenu.listaProducto[i].getValor()+" - "+this.ventanaMenu.listaProducto[i].getCantidad());
                contenedorPrincipal.add(etq_lista);
            }
        }
        revalidate();
    }
