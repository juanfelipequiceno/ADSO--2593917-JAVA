    private void registraUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registraUsuarioActionPerformed
        String id = campoId.getText();
        String nombre = campoNombre.getText();
        String cantidad = campoCantidad.getText();
        String valor = campoValor.getText();
   
        if (!id.equals("") && !nombre.equals("") && !cantidad.equals("") && !valor.equals("")) {
            Producto temporal = new Producto(id, nombre, cantidad, valor);
            this.ventanaMenu.listaProducto[this.ventanaMenu.indexproducto] = temporal;
            this.ventanaMenu.indexproducto++;
            dispose();
            this.ventanaMenu.setVisible(true);
            
            System.out.println("Registro con exito");
        }else{
            Mensaje mensaje = new Mensaje("¡FALTAN DATOS!", "Todos Los campos son necesarios...!", "INCORRECTO");
        }
    }//GEN-LAST:event_registraUsuarioActionPerformed


